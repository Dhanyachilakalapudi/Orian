function LiveFeed({ messages }) {
  const feedRef = useRef(null);

  useEffect(() => {
    if (feedRef.current) {
      feedRef.current.scrollTop = feedRef.current.scrollHeight;
    }
  }, [messages]);

  return (
    <div className="livefeed">
      <div className="livefeed-header">
        <div>
          <h2 className="livefeed-title">agent activity</h2>
          <p className="livefeed-codename">/ codename: hive /</p>
        </div>
        <div className="livefeed-badge">
          <span className="livefeed-dot" />
          processing
        </div>
      </div>
      <div className="livefeed-messages" ref={feedRef}>
        {messages.map((msg, i) => (
          <div key={i} className="livefeed-message">
            <span className="livefeed-agent">
              <span className="livefeed-icon">{AgentIcons[msg.agent] || <DefaultIcon />}</span>
              {msg.agent}
            </span>
            <span className="livefeed-text">{msg.message}</span>
          </div>
        ))}
      </div>
    </div>
  );
}

export default LiveFeed;
